int		ft_strcmp(const char *s1, const char *s2)
{
	int i;

	if (!s1 || !s2)
		return (0);
	i = 0;
	if (s1[i] != s2[i])
		return (s1[i] - s2[i]);
	while (s1[i] == s2[i])
	{
		i++;
		if ((s1[i - 1]) == '\0')
			return (0);
	}
	return ((unsigned char)s1[i] - (unsigned char)s2[i]);
}
