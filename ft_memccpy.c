#include "libft.h"

void	*ft_memccpy(void *dst, const void *src, int c, size_t n)
{
	size_t i;

	i = 0;
	while (i < n)
	{
		((char*)dst)[i] = ((char*)src)[i];
		if (((const char*)src)[i++] == (unsigned char)c)
			return ((void*)((char*)dst + i));
	}
	return (NULL);
}
